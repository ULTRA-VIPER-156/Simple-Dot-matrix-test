const canvas = document.getElementById('dotMatrix'); // Fixed ID
const ctx = canvas.getContext('2d');

const pixelSize = 20; // Size of each pixel square

function drawMatrix(matrix) {
  ctx.clearRect(0, 0, canvas.width, canvas.height);

  const radius = pixelSize / 2; // half the pixel size for perfect circles
  for (let y = 0; y < matrix.length; y++) {
    for (let x = 0; x < matrix[y].length; x++) {
      ctx.beginPath();
      const centerX = x * pixelSize + radius;
      const centerY = y * pixelSize + radius;

      if (matrix[y][x] === 1) {
        ctx.fillStyle = 'blue'; // black filled circle
      } else {
        ctx.fillStyle = 'grey'; // white background circle
      }

      ctx.arc(centerX, centerY, radius * 0.8, 0, Math.PI * 2); // slightly smaller radius for padding
      ctx.fill();
      ctx.closePath();
    }
  }
}



drawMatrix(pixelMatrix);
function updateMatrix()
 {
  ///update each pixel in the matrix 

  for (let y = 0; y < pixelMatrix.length; y++) 
    {
     for (let x=0; x < pixelMatrix[y].length; x++)
      {
       
        //delay the time it takes to update the matrix
        setTimeout(function() {
          pixelMatrix[y][x] = Math.random() < 0.5 ? 1 : 0; // Randomly set each pixel to 1 or 0
        }, Math.random() * 1000); // Random delay between 0 and 1000 milliseconds
      }

  }
 
  
  
  drawMatrix(pixelMatrix);
}
setInterval(updateMatrix, 1000); // Update every second
document.addEventListener('DOMContentLoaded', () => {
  const button = document.querySelector('button');
  if (button) {
    button.addEventListener('click', () => {
      updateMatrix();
    });
  }
});



